<h2>Como coletamos informações a seu respeito</h2>

Quando você visita o site da CODE CONHECIMENTO LTDA ou usa os Serviços da CODE CONHECIMENTO LTDA, coletamos o seu endereço IP e as informações padrões de acesso à web como o tipo do seu navegador e as páginas que acessou em nosso site. Se você se inscrever, coletaremos os seguintes tipos de informações:
<br/>
    Informações de contato – o seu nome, telefone e e-mail.
<br/>
Nós não coletamos informações do tipo:
<br/>
    Informações financeiras – não coletamos informações financeiras, tais como números da conta bancária e de cartão de crédito. Quando você adquire nossos produtos, a plataforma de pagamentos que contratamos coleta essas informações e nós não temos acesso a elas em nenhum momento.

<h2>Como usamos cookies</h2>

Quando você acessa nosso site, nós, incluindo as empresas que contratamos para acompanhar como nosso site é usado, podemos colocar pequenos arquivos de dados chamados “cookies” no seu computador. Enviamos um “cookie da sessão” para o seu computador quando você entra em sua conta ou usa os Serviços da CODE CONHECIMENTO LTDA. Esse tipo de cookie nos ajuda a reconhecê-lo se visitar várias páginas em nosso site durante a mesma sessão. Depois que você sair ou fechar o seu navegador, esse cookie irá expirar e deixará de ter efeito. Codificamos nossos cookies para que apenas nós possamos interpretar as informações armazenadas neles. Você está livre para recusar nossos cookies caso o seu navegador permita. Nós e nossos prestadores de serviço também usamos cookies para personalizar nossos serviços, conteúdo e publicidade, avaliar a eficiência das promoções e promover confiança e segurança. Você pode encontrar cookies de terceiros ao usar os Serviços da CODE CONHECIMENTO LTDA em determinados sites que não estão sob nosso controle (por exemplo, se você visualizar uma página da Web criada por terceiros ou usar um aplicativo desenvolvido por terceiros, um cookie poderá ser colocado por essa página ou aplicativo).

<h2>Remarketing do Google</h2>

Nós utilizamos o recurso de Remarketing do Google Adwords para veicular anúncios em sites de parceiros do Google. Este recurso permite identificar que você visitou o nosso site e assim o Google pode exibir o nosso anúncio para você em diferentes websites. Diversos fornecedores de terceiros, inclusive o Google, compram espaços de publicidade em sites da Internet. Nós eventualmente contratamos o Google para exibir nossos anúncios nesses espaços. Para identificar a sua visita no nosso site, tanto outros fornecedores de terceiros, quanto o Google, utilizam-se de cookies, de forma similar ao exposto na seção “Como usamos cookies”. Você pode desativar o uso de cookies pelo Google acessando o <a target="https://www.google.com/ads/preferences/">Gerenciador de preferências de anúncio</a>.

<h2>Como protegemos e armazenamos informações pessoais</h2>

Ao longo desta política, usamos o termo “informações pessoais” para descrever informações que possam ser associadas a uma determinada pessoa e possam ser usadas para identificar essa pessoa. Nós não consideraremos como informações pessoais as informações que devem permanecer anônimas, para que elas não identifiquem um determinado usuário. Armazenamos e processamos suas informações pessoais em nossos computadores e as protegemos sob medidas de proteção físicas, eletrônicas e processuais. Usamos proteções de computador, como firewalls e criptografia de dados, aplicamos controles de acesso físico a nossos prédios e arquivos e autorizamos o acesso a informações pessoais apenas para os funcionários que precisem delas para cumprir suas responsabilidades profissionais.

<h2>Como usamos as informações pessoais que coletamos</h2>

Nossa finalidade principal ao coletar informações pessoais é fornecer a você uma experiência segura, tranquila, eficiente e personalizada. Para isso, usamos suas informações pessoais para:
<br/>
    fornecer os serviços e o suporte ao cliente solicitados;<br/>
    processar transações e enviar avisos sobre as suas transações<br/>
    solucionar disputas, cobrar taxas e solucionar problemas;<br/>
    impedir atividades potencialmente proibidas ou ilegais e garantir a aplicação do nosso Contrato do usuário;<br/>
    personalizar, avaliar e melhorar nossos serviços, além do conteúdo e do layout do nosso site;<br/>
    enviar materiais de marketing direcionados, avisos de atualização no serviço e ofertas promocionais com base nas suas preferências de comunicação;
    comparar informações, para uma maior precisão, e verificá-las com terceiros.

<h2>Marketing</h2>

Não vendemos nem alugamos suas informações pessoais para terceiros para fins de marketing sem seu consentimento explícito. Podemos combinar suas informações com as informações que coletamos de outras empresas e usá-las para melhorar e personalizar nossos serviços, conteúdo e publicidade. Se não desejar receber nossas mensagens de marketing nem participar de nossos programas de personalização de anúncios, basta indicar sua preferência mandando-nos um e-mail ou simplesmente clicar no link de descadastramento fornecido em todos os nossos emails.

